def _read_one_liner(path: Path) -> str:
    """Extract the first blockquote after the distillate:start marker."""
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return ""
    start = text.find("<!-- distillate:start -->")
    if start < 0:
        return ""
    after = text[start + 24:]
    for line in after.strip().split("\n"):
        line = line.strip()
        if line.startswith("> ") and len(line) > 10:
            return line[2:]
    return ""


def _read_frontmatter_tags(path: Path) -> list[str]:
    """Extract topic tags from frontmatter (lowercased, skip author names)."""
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return []
    if not text.startswith("---"):
        return []
    end = text.find("\n---", 4)
    if end < 0:
        return []
    fm = text[4:end]
    tags: list[str] = []
    in_tags = False
    for line in fm.split("\n"):
        if line.startswith("tags:"):
            in_tags = True
            continue
        if in_tags:
            if line.startswith("  - "):
                tag = line.strip().removeprefix("- ").strip('"').strip("'")
                # Skip author names (capitalized first letter)
                if tag and (tag[0].islower() or "/" in tag):
                    tags.append(tag)
            else:
                break
    return tags


def _count_notebook_entries(path: Path) -> int:
    """Count high-signal entries in a lab notebook day file."""
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return 0
    return sum(1 for line in text.split("\n")
               if line.startswith("- **") or line.startswith("## "))


def regenerate_index() -> Path | None:
    """Rebuild ``index.md`` from the vault filesystem.

    Produces a research home page: papers grouped by recency with
    one-liner summaries, experiments with run counts, recent notebook
    days with entry counts, and a To Read queue size. Preserves user
    content outside the managed block. Returns the path or None.
    """
    root = _vault_root()
    if root is None:
        return None

    try:
        now = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
        sections: list[str] = []

        # -- Papers ----------------------------------------------------------
        saved = root / "Papers" / "Notes"
        papers: list[dict[str, str]] = []
        all_tags: dict[str, int] = {}
        if saved.is_dir():
            for p in sorted(saved.glob("*.md")):
                fm = _read_frontmatter_fields(
                    p, {"title", "date_read", "engagement", "citekey", "authors"},
                )
                title = fm.get("title", p.stem)
                citekey = fm.get("citekey", p.stem)
                one_liner = _read_one_liner(p)
                tags = _read_frontmatter_tags(p)
                for t in tags:
                    all_tags[t] = all_tags.get(t, 0) + 1
                papers.append({
                    "citekey": citekey,
                    "title": title,
                    "authors": fm.get("authors", ""),
                    "date_read": fm.get("date_read", ""),
                    "engagement": fm.get("engagement", ""),
                    "one_liner": one_liner,
                    "tags": tags,
                })

        # To Read queue
        to_read = root / "Papers" / "To Read"
        to_read_count = 0
        if to_read.is_dir():
            to_read_count = sum(1 for _ in to_read.glob("*.pdf"))

        if papers or to_read_count:
            lines = [f"## Papers\n"]
            lines.append(f"**{len(papers)}** read")
            if to_read_count:
                lines.append(f" · **{to_read_count}** in [[Papers/To Read|To Read]]")
            if all_tags:
                top_tags = sorted(all_tags.items(), key=lambda x: -x[1])[:6]
                tag_str = ", ".join(f"`{t}`" for t, _ in top_tags)
                lines.append(f" · Topics: {tag_str}")
            lines.append("\n")

            for p in sorted(papers, key=lambda x: x["date_read"] or "", reverse=True):
                link = f"[[Papers/Notes/{p['citekey']}|{p['title']}]]"
                meta_parts = []
                if p["authors"]:
                    meta_parts.append(p["authors"].split(",")[0].strip())
                if p["date_read"]:
                    meta_parts.append(p["date_read"])
                if p["engagement"]:
                    meta_parts.append(f"{p['engagement']}%")
                meta = " · ".join(meta_parts)
                lines.append(f"- {link}")
                if meta:
                    lines.append(f"  {meta}")
                if p["one_liner"]:
                    lines.append(f"  *{p['one_liner'][:200]}*")
            sections.append("\n".join(lines))

        # -- Experiments -----------------------------------------------------
        projects_dir = root / "Experiments"
        projects: list[dict[str, str]] = []
        if projects_dir.is_dir():
            for p in sorted(projects_dir.glob("*.md")):
                if p.name.startswith("."):
                    continue
                # Rough size as a proxy for activity
                size = p.stat().st_size
                projects.append({
                    "id": p.stem,
                    "name": p.stem,
                    "size": f"{size // 1024}K" if size > 1024 else f"{size}B",
                })

        if projects:
            lines = [f"## Experiments ({len(projects)})\n"]
            for p in sorted(projects, key=lambda x: x["name"]):
                lines.append(
                    f"- [[Experiments/{p['id']}|{p['name']}]] · {p['size']}"
                )
            sections.append("\n".join(lines))

        # -- Lab Notebook (recent 14 days) -----------------------------------
        nb_dir = root / "Lab Notebook"
        days: list[tuple[str, int]] = []
        if nb_dir.is_dir():
            for p in sorted(nb_dir.glob("*.md"), reverse=True):
                if re.match(r"\d{4}-\d{2}-\d{2}\.md$", p.name):
                    count = _count_notebook_entries(p)
                    days.append((p.stem, count))
                if len(days) >= 14:
                    break

        if days:
            lines = ["## Lab Notebook\n"]
            for d, count in days:
                label = f"{count} entries" if count != 1 else "1 entry"
                lines.append(f"- [[Lab Notebook/{d}|{d}]] · {label}")
            sections.append("\n".join(lines))

        managed = "\n\n".join(sections) + "\n" if sections else ""

        # -- Assemble file ---------------------------------------------------
        path = root / "index.md"
        header = (
            "# Research Wiki\n\n"
            f"> Last updated: {now}\n\n"
        )
        block = f"{_MANAGED_BEGIN}\n{managed}{_MANAGED_END}\n"

        if path.exists():
            existing = path.read_text(encoding="utf-8")
            if _MANAGED_BEGIN in existing and _MANAGED_END in existing:
                start = existing.index(_MANAGED_BEGIN)
                end = existing.index(_MANAGED_END) + len(_MANAGED_END)
                before = existing[:start].rstrip()
                after = existing[end:].lstrip("\n")
                prefix = before + "\n\n" if before else header
                suffix = "\n" + after if after else ""
                final = prefix + block + suffix
            else:
                final = header + block
        else:
            final = header + block

        path.write_text(final, encoding="utf-8", newline="\n")
        log.info("Rebuilt index.md (%d papers, %d experiments, %d days)",
                 len(papers), len(projects), len(days))
        return path
    except Exception:
        log.debug("Failed to rebuild index.md", exc_info=True)
        return None


