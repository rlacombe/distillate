def vault_lint() -> dict:
    """Check vault health and return a diagnostic report.

    Returns a dict with ``configured``, ``issues`` (list of strings),
    and ``stats`` (counts of papers, projects, notebook days).
    """
    root = _vault_root()
    if root is None:
        return {"configured": False, "issues": [], "stats": {}}

    issues: list[str] = []
    stats: dict[str, int] = {}

    # -- Papers with missing frontmatter ------------------------------------
    saved = root / "Papers" / "Notes"
    paper_count = 0
    if saved.is_dir():
        for p in saved.glob("*.md"):
            paper_count += 1
            text = p.read_text(encoding="utf-8", errors="replace")[:50]
            if not text.startswith("---"):
                issues.append(f"Paper note missing frontmatter: {p.name}")
    stats["papers"] = paper_count

    # -- Orphan PDFs (no matching .md) --------------------------------------
    pdf_dir = saved / (config.PDF_SUBFOLDER or "pdf") if saved.is_dir() else None
    if pdf_dir and pdf_dir.is_dir():
        note_stems = {p.stem for p in saved.glob("*.md")} if saved.is_dir() else set()
        for pdf in pdf_dir.glob("*.pdf"):
            if pdf.stem not in note_stems:
                issues.append(f"Orphan PDF (no paper note): {pdf.name}")

    # -- Projects -----------------------------------------------------------
    projects_dir = root / "Experiments"
    project_count = 0
    if projects_dir.is_dir():
        project_count = sum(1 for _ in projects_dir.glob("*.md"))
    stats["experiments"] = project_count

    # -- Lab Notebook days --------------------------------------------------
    nb_dir = root / "Lab Notebook"
    nb_count = 0
    if nb_dir.is_dir():
        nb_count = sum(1 for p in nb_dir.glob("*.md")
                       if re.match(r"\d{4}-\d{2}-\d{2}\.md$", p.name))
    stats["notebook_days"] = nb_count

    # -- Structural files ---------------------------------------------------
    if not (root / "_meta" / "schema.md").exists():
        issues.append("Missing _meta/schema.md — run generate_schema()")
    if not (root / "index.md").exists():
        issues.append("Missing index.md — run regenerate_index()")
    if not (root / "Papers Log.md").exists():
        issues.append("Missing Papers Log.md — will be created on next paper save")

    from distillate.obsidian import _needs_template_update, _TEMPLATE_VERSION
    for name in ("_meta/Stats.md", "_meta/Papers.base"):
        path = root / name
        if path.exists() and _needs_template_update(path):
            issues.append(f"Stale template: {name} (needs v{_TEMPLATE_VERSION})")

    stats["issues"] = len(issues)
    return {"configured": True, "issues": issues, "stats": stats}
