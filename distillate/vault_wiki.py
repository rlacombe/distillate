"""Vault wiki — Karpathy-pattern schema, index, and lint for the Obsidian vault.

Implements the three structural pillars of a personal knowledge wiki:

- **schema.md** — describes folder layout, note types, and conventions so
  that any LLM operating on the vault knows the rules.
- **index.md** — auto-maintained content catalog with wikilinks to every
  entity (papers, projects, recent notebook days).  The LLM reads this
  first when answering queries.  At moderate scale it replaces RAG.
- **vault_lint()** — periodic health check: orphan PDFs, missing
  frontmatter, stale templates, missing structural files.

All functions are no-ops when ``config.OBSIDIAN_VAULT_PATH`` is empty.
"""

from __future__ import annotations

import logging
import re
from datetime import datetime, timezone
from pathlib import Path

from distillate import config

log = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _vault_root() -> Path | None:
    """Return ``{vault}/{papers_folder}/`` or None if no vault configured."""
    if not config.OBSIDIAN_VAULT_PATH:
        return None
    return Path(config.OBSIDIAN_VAULT_PATH) / config.OBSIDIAN_PAPERS_FOLDER


_MANAGED_BEGIN = "<!-- distillate:index -->"
_MANAGED_END = "<!-- /distillate:index -->"


def _read_frontmatter_fields(path: Path, fields: set[str]) -> dict[str, str]:
    """Extract specific scalar frontmatter values from a markdown file.

    Lightweight parser — does not depend on PyYAML.  Reads only the first
    ``---``-delimited block and returns matching keys as stripped strings.
    List values (like ``authors``) are joined with ``, ``.
    """
    try:
        text = path.read_text(encoding="utf-8", errors="replace")
    except OSError:
        return {}
    if not text.startswith("---"):
        return {}
    end = text.find("\n---", 4)
    if end < 0:
        return {}
    fm = text[4:end]

    result: dict[str, str] = {}
    current_key: str | None = None
    list_items: list[str] = []

    for line in fm.split("\n"):
        if line and not line[0].isspace() and ":" in line:
            # Flush previous key
            if current_key and current_key in fields:
                if list_items:
                    result[current_key] = ", ".join(list_items)
                    list_items = []
            key, _, val = line.partition(":")
            current_key = key.strip()
            val = val.strip().strip('"').strip("'")
            if current_key in fields and val:
                result[current_key] = val
        elif line.strip().startswith("- ") and current_key in fields:
            list_items.append(line.strip().removeprefix("- ").strip('"').strip("'"))

    if current_key and current_key in fields and list_items:
        result[current_key] = ", ".join(list_items)

    return result


# ---------------------------------------------------------------------------
# Schema
# ---------------------------------------------------------------------------

_SCHEMA_VERSION = "2"

_SCHEMA_TEMPLATE = """\
---
distillate_schema: "{version}"
---

# Distillate Vault Schema

> This file describes the structure and conventions of the Distillate-managed
> portion of this Obsidian vault.  It is auto-generated by Distillate and
> updated when the schema version changes.  You may edit it to teach
> Distillate (and any LLM operating on this vault) your preferred conventions;
> your edits will be preserved across updates.

## Folder Structure

```
{folder}/
├── Papers/
│   ├── To Read/      Unprocessed PDFs awaiting reading
│   ├── Notes/        Paper notes (one .md per paper, citekey-named)
│   └── pdf/          Annotated PDFs
├── Experiments/      Experiment notebooks (one .md per project)
│   └── html/         HTML visualizations
├── Lab Notebook/     Daily research journal (curated, sectioned)
├── _meta/
│   ├── schema.md     This file — vault conventions
│   ├── Stats.md      Dataview-powered analytics dashboard
│   └── Papers.base   Obsidian Bases database view
├── index.md          Auto-maintained content catalog (read this first)
└── Papers Log.md     Chronological reading log
```

## Note Types

### Paper Notes  (`Papers/Notes/*.md`)

Frontmatter fields: `title`, `citekey`, `authors`, `date_added`,
`date_read`, `year`, `zotero`, `doi`, `journal`, `engagement`,
`highlight_word_count`, `page_count`, `tags`.

Body: summary, key learnings, abstract, highlights, handwritten notes.
Managed between `<!-- distillate:start -->` and `<!-- distillate:end -->`.
User content goes in the **My Notes** section after the end marker.

### Experiment Notebooks  (`Experiments/*.md`)

One per experiment, named by project ID.  Content between
`<!-- distillate:start -->` / `<!-- distillate:end -->` markers.
May have section variants: `{{id}}-{{section}}.md`.

### Lab Notebook  (`Lab Notebook/*.md`)

One per day, named `YYYY-MM-DD.md`.  Curated digest from the local
knowledge base — not every raw entry, only high-signal research events.
Managed between `<!-- distillate:managed -->` / `<!-- /distillate:managed -->`.
Sections: Notes, Papers, Experiments, Sessions.

## Linking Conventions

- Paper references: `[[citekey|title]]` (Obsidian resolves via `aliases`)
- Experiment references: `[[Experiments/id|name]]`
- Lab notebook cross-links use these conventions automatically

## Content Ownership

Distillate manages content **between its marker comments** and never
touches content outside them.  You can safely add your own notes,
sections, and files anywhere in this folder.
"""


def generate_schema() -> Path | None:
    """Write ``_meta/schema.md`` if missing or outdated. Returns path or None."""
    root = _vault_root()
    if root is None:
        return None

    meta = root / "_meta"
    meta.mkdir(parents=True, exist_ok=True)
    path = meta / "schema.md"
    folder = config.OBSIDIAN_PAPERS_FOLDER

    # Migrate from root-level schema.md
    old = root / "schema.md"
    if old.exists() and not path.exists():
        old.rename(path)
        log.info("Migrated schema.md -> _meta/schema.md")

    # Check version — only overwrite on version bump or missing
    if path.exists():
        head = path.read_text(encoding="utf-8")[:200]
        if f'distillate_schema: "{_SCHEMA_VERSION}"' in head:
            return path  # up to date

    try:
        content = _SCHEMA_TEMPLATE.format(version=_SCHEMA_VERSION, folder=folder)
        path.write_text(content, encoding="utf-8", newline="\n")
        log.info("Wrote _meta/schema.md (v%s)", _SCHEMA_VERSION)
        return path
    except Exception:
        log.debug("Failed to write schema.md", exc_info=True)
        return None


